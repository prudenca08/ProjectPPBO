    private void tablePelangganMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablePelangganMouseClicked
        // TODO add your handling code here:
        comboSopir.removeAllItems();
        comboMobil.removeAllItems();
        comboMobil.addItem("-Pilih Mobil");
        comboSopir.addItem("-Pilih Sopir");
        tampilCombo();
        fieldKTP.setEditable(false);
        fieldKTP.requestFocus();
        int i = tablePelanggan.getSelectedRow();
        if (i >- 1) {
            try {
                ResultSet rs = conn.createStatement().executeQuery("SELECT * FROM pelanggan WHERE no_transaksi = '" 
                    + tabmode.getValueAt(i, 0).toString() + "'");
                while (rs.next()) {
                    fieldKTP.setText(rs.getString("no_ktp"));
                    fieldSewa.setText(rs.getString("hari_sewa"));
                    if ("Pria".equalsIgnoreCase(rs.getString("jenis_kelamin"))) {
                        radioPria.setSelected(true);
                    } else {
                        radioWanita.setSelected(true);
                    }
                }
            } catch (SQLException ex) {
            Logger.getLogger(Pelanggan.class.getName()).log(Level.SEVERE, null, ex);
            }
            fieldNama.setText(tabmode.getValueAt(i, 1).toString());
            
            fieldTelp.setText(tabmode.getValueAt(i, 2).toString());
            fieldAlamat.setText(tabmode.getValueAt(i, 3).toString());
            
            comboMobil.addItem((tabmode.getValueAt(i, 4).toString()) + " (" + (tabmode.getValueAt(i, 5).toString()) + ")");
            comboMobil.setSelectedItem((tabmode.getValueAt(i, 4).toString()) + " (" + (tabmode.getValueAt(i, 5).toString()) + ")");
            comboSopir.addItem((tabmode.getValueAt(i, 6).toString()) + " (" + (tabmode.getValueAt(i, 7).toString()) + ")");
            comboSopir.setSelectedItem((tabmode.getValueAt(i, 6).toString()) + " (" + (tabmode.getValueAt(i, 7).toString()) + ")");
            
            fieldTanggal.setText(tabmode.getValueAt(i, 9).toString());
            
            if ("Lunas".equalsIgnoreCase(tabmode.getValueAt(i, 10).toString())) {
                radioLunas.setSelected(true);
            } else {
                radioDP.setSelected(true);
            }
            
        }
    }//GEN-LAST:event_tablePelangganMouseClicked

    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        // TODO add your handling code here:
        String tanggal = fieldTanggal.getText();
        String noTransaksi = tanggal.replace("-", "") + fieldKTP.getText();
        
        String JK;
        if (radioPria.isSelected()) {
            JK = "Pria";
        } else if (radioWanita.isSelected()) {
            JK = "Wanita";
        } else {
            JK = "";
        }
        
        String Ket;
        int Profit;
        if (radioDP.isSelected()) {
            Ket = "DP 50 %";
            Profit = 100000;
        } else if (radioLunas.isSelected()) {
            Ket = "Lunas";
            Profit = 200000;
        } else {
            Ket = "";
            Profit = 0;
        }
        if ( Profit != 0)
            JOptionPane.showMessageDialog(null, "Total Pembayaran Rp. " 
                    + Integer.parseInt(fieldSewa.getText()) * Profit);
        
         if ("".equals(fieldKTP.getText()) || "".equals(fieldNama.getText()) || 
                "".equals(fieldSewa.getText()) || "".equals(fieldTelp.getText())
                || "".equals(fieldAlamat.getText()) || "".equals(fieldTanggal.getText()) ) {
            JOptionPane.showMessageDialog(this, 
                    "Harap melengkapi data terlebih dahulu", "ERROR", 
                    JOptionPane.WARNING_MESSAGE);
        } else if ("".equals(Ket) || "".equals(JK) ||
                "-Pilih Mobil".equals(comboMobil.getSelectedItem())) {
            JOptionPane.showMessageDialog(this, 
                    "Harap memilih data terlebih dahulu", "ERROR", 
                    JOptionPane.WARNING_MESSAGE);
        } else {
            
            String cMblStr = (String) comboMobil.getSelectedItem();
            int awalM = cMblStr.indexOf("(");
            int akhirM = cMblStr.indexOf(")");
            String noPlat = cMblStr.substring(awalM+1, akhirM);
            String namaMobil = cMblStr.substring(0, awalM-1);

            String cSprStr = (String) comboSopir.getSelectedItem();
            String noHP;
            String namaSopir;
             if (cSprStr == "-Pilih Sopir") {
                 noHP = "";
                 namaSopir = "";
             } else {
                 int awalS = cSprStr.indexOf("(");
                 int akhirS = cSprStr.indexOf(")");
                 noHP = cSprStr.substring(awalS+1, akhirS);
                 namaSopir = cSprStr.substring(0, awalS-1);
             }
            
            try {
            conn.createStatement().executeUpdate("INSERT INTO pelanggan SET "
                    + "no_transaksi = '" + noTransaksi + "',"
                    + "no_ktp = '" + fieldKTP.getText() + "',"
                    + "nama = '" + fieldNama.getText() + "'," 
                    + "jenis_kelamin = '" + JK + "'," 
                    + "no_telp = '" + fieldTelp.getText() + "',"
                    + "alamat = '" + fieldAlamat.getText() + "',"
                    + "hari_sewa = '" + fieldSewa.getText() + "',"
                    + "mobil = '" + namaMobil + "',"
                    + "no_plat = '" + noPlat + "',"
                    + "sopir = '" + namaSopir + "',"
                    + "no_telp_sopir = '" + noHP + "',"
                    + "tgl_kembali = '" + fieldTanggal.getText() + "',"
                    + "profit = '" + Integer.parseInt(fieldSewa.getText()) * Profit + "',"
                    + "ket = '" + Ket 
                    + "';");
            comboSopir.removeAllItems();
            comboMobil.removeAllItems();
            tampilkan();
            reset();
            tampilCombo();
            fieldKTP.requestFocus();
            JOptionPane.showMessageDialog(null, "Data berhasil disimpan");
        } catch (SQLException ex) {
            Logger.getLogger(Pelanggan.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Data gagal disimpan, " + ex);
        }
        }
    }//GEN-LAST:event_btnTambahActionPerformed

