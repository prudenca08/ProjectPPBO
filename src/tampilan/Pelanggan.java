    private void tampilCombo(){
        try {
            conn.createStatement().executeUpdate("UPDATE mobil SET "
                    + "status = 'Tersedia'");
            conn.createStatement().executeUpdate("UPDATE sopir SET "
                    + "ket = 'Ada'");
            
            ResultSet rsPelanggan = conn.createStatement().executeQuery("SELECT no_plat, no_telp_sopir FROM pelanggan");
            
            while (rsPelanggan.next()) {
                conn.createStatement().executeUpdate("UPDATE mobil SET "
                    + "status = 'Disewa'"
                    + " WHERE plat = '" + rsPelanggan.getString("no_plat")
                    + "'");
                conn.createStatement().executeUpdate("UPDATE sopir SET "
                    + "ket = 'Tidak Ada'"
                    + " WHERE no_telp = '" + rsPelanggan.getString("no_telp_sopir")
                    + "'");
            }
            
            
            ResultSet rsMobil = conn.createStatement().executeQuery("SELECT * FROM mobil WHERE status = 'Tersedia'");
            while (rsMobil.next()) {
                String namaMobil = rsMobil.getString("nama");
                String noPlat = rsMobil.getString("plat");
                comboMobil.addItem(namaMobil + " (" + noPlat + ")");
            }
            
            ResultSet rsSopir = conn.createStatement().executeQuery("SELECT * FROM sopir WHERE ket = 'Ada'");
            while (rsSopir.next()) {
                String nama = rsSopir.getString("nama");
                String no = rsSopir.getString("no_telp");
                comboSopir.addItem(nama + " (" + no + ")");
            }
         } catch (SQLException ex) {
            Logger.getLogger(Pelanggan.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
